            </td>
            <td :style="!printLayout ? 'border: 2px solid black' : ''">
              {{
                reciept.total_amount
                  ? reciept.total_amount.toLocaleString('en-US')
                  : ''
              }}
            </td>
          </tr>
        </tbody>
      </table>
      <div v-if="reciept">
        <p
          :style="printLayout ? { width: '20rem' } : { width: '48rem' }"
          style="text-align: end"
        >
          Total: {{ numberWithCommas(reciept.total_amount) }}
        </p>
      </div>
    </div>
  </div>
</template>

<script>
import { DateTime } from 'luxon'
export default {
  props: {
    reciept: {
      type: Object,
      require: false,
      default: () => ({}),
    },
    data: {
      type: Object,
      require: false,
      default: () => ({}),
    },
    printLayout: {
      type: Boolean,
      require: false,
      default: false,
    },
  },
  computed: {
    transactionDate() {
      if (this.reciept.created_at) {
        return DateTime.fromISO(this.reciept.created_at).toFormat(
          'yyyy-LL-dd T'
        )
      }
    },
    address() {
      return process.env.ADDRESS
    },
    companyName() {
      return process.env.COMPANY_NAME
    },
    logo() {
      return process.env.LOGO
    },
    tel() {
      return process.env.TEL
    },
  },
  methods: {
    numberWithCommas(x) {
      return x.toString().replace(/\B(?<!\.\d*)(?=(\d{3})+(?!\d))/g, ',')
    },
    convDate(x) {
      return DateTime.fromISO(x).toFormat('yy-LL-dd T')
    },
  },
}
</script>

