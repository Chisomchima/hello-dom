<template>
  <div class="filter-wrapper">
    <div class="row">
      <div class="col-md-4">
        <div class="mb-2">
          <label class="form-control-label">Service Center</label>
          <VSelect
            v-model="filters.service_center"
            label="name"
            :multiple="true"
            :reduce="(opt) => opt.id"
            :options="service_centers"
          />
        </div>
      </div>
      <div class="col-md-4">
        <div class="mb-2">
          <label class="form-control-label">Modality</label>
          <VSelect
            v-model="filters.modality"
            label="name"
            :multiple="true"
            :reduce="(opt) => opt.id"
            :options="modalities"
          />
        </div>
      </div>
      <div class="col-md-4">
        <div class="mb-2">
          <label class="form-control-label">Status</label>
          <VSelect v-model="filters.status" label="name" :options="statuses" :reduce="(opt) => opt.val" />
        </div>
      </div>

    </div>

    <div class="row justify-content-end">

      <div class="col-md-4">
        <div class="row mt-4">
          <div class="col-6">
            <BaseButton class="w-100" @click="filterFunc(filters)">
              Search
            </BaseButton>
          </div>
          <div class="col-6">
            <BaseButton class="w-100 btn-danger" @click="clear()">
              Clear
            </BaseButton>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { debounce } from 'lodash'

export default {
  data() {
    return {
      genders: ['Male', 'Female'],
      statuses: [{name: 'New', val: 'NEW'}, {name: 'Captured', val: 'CAPTURED'}, {name: 'Awaiting approval', val: 'AWAITING_APPROVAL'}],
      service_centers: [],
      modalities: [],
      filters: {
        service_center: [],
        modality: [],
        status: '',
        // asn: '',
        // patient_uhid: '',
        // patient_name: '',
        // patient_phone: '',
      },
    }
  },
  watch: {
    'filters.status': {
      handler: debounce(function () {
        this.filterFunc(this.filters)
      }, 500),
      deep: true,
    },
    
    'filters.modality': {
      handler: debounce(function () {
        this.filterFunc(this.filters)
      }, 500),
      deep: true,
    },
     'filters.service_center': {
      handler: debounce(function () {
        this.filterFunc(this.filters)
      }, 500),
      deep: true,
    },

    genders: {},
  },
  created() {
    if (this.$route.query.filter) {
      this.filters = JSON.parse(this.$route.query.filter)
    }
    this.filterFunc(this.filters)

    this.getServiceCenters()
    this.getModality()
  },
  methods: {
    clear() {
      this.filters = {
        service_center: '',
        modality: '',
        status: '',
        asn: '',
        patient_uhid: '',
        // patient_name: '',
        // patient_phone: '',
      }
      this.filterFunc(this.filters)
    },

    filterFunc(newVal) {
      this.$router.push({
        name: this.$router.name,
        query: {
          ...this.$router.query,
          filter: JSON.stringify(newVal),
        },
      })
    },
    async getServiceCenters() {
      try {
        const data = await this.$api.imaging.getServiceCenter({ size: 1000 })
        this.service_centers = data.results
      } catch (error) {
        console.log(error)
      }
    },

    async getModality() {
      try {
        const data = await this.$api.imaging.getLabUnit({ size: 1000 })
        this.modalities = data.results
      } catch (error) {
        console.log(error)
      }
    },
  },
}
</script>

